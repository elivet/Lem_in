int			ft_sizeofint(int nbr)
{
	int		size;

	size = 1;
	if (nbr == 0)
		return (1);
	if (nbr > 0)
	{
		while ((nbr / 10) != 0)
		{
			nbr = nbr / 10;
			size++;
		}
	}
	else
	{
		while ((nbr / 10) != 0)
		{
			nbr = nbr / 10;
			size++;
		}
		size++;
	}
	return (size);
}
